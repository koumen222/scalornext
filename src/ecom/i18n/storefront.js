const _defaultTextIndex = new Map();
DEFAULT_TEXTS.forEach((row) => {
  row.forEach((variant) => _defaultTextIndex.set(variant.trim().toLowerCase(), row));
});

/**
 * Localise un texte marchand S'IL correspond à un défaut connu du form builder,
 * sinon le renvoie tel quel (personnalisations respectées).
 */
export function localizeMerchantDefault(language, value) {
  if (typeof value !== 'string' || !value.trim()) return value;
  const row = _defaultTextIndex.get(value.trim().toLowerCase());
  if (!row) return value;
  const idx = { fr: 0, en: 1, es: 2 }[normalizeStoreLanguage(language)] ?? 0;
  return row[idx] ?? value;
}

export function normalizeStoreLanguage(value) {
  const raw = String(value || '').trim().toLowerCase().slice(0, 2);
  return MESSAGES[raw] ? raw : 'fr';
}

/** t(key, vars) — repli : fr, puis la clé elle-même (jamais de crash sur clé manquante). */
export function getStorefrontT(language) {
  const lang = normalizeStoreLanguage(language);
  const dict = MESSAGES[lang];
  return function t(key, vars) {
    let msg = dict[key] ?? MESSAGES.fr[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        msg = msg.replaceAll(`{${k}}`, String(v));
      });
    }
    return msg;
  };
}
