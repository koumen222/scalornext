'use client';

/**
 * i18n du chrome storefront — fr (défaut) / en / es.
 * La langue vient du réglage marchand `storeSettings.language` (Paramètres boutique),
 * exposé par l'API publique dans `store.language` (cf. BACKEND_PATCH_I18N.md).
 *
 * Ne concerne QUE les chaînes en dur du storefront (boutons, totaux, erreurs…).
 * Les textes saisis par le marchand (labels de champs, contenus produits) restent tels quels.
 *
 * Usage : const t = getStorefrontT(store?.language);  t('cta.orderNow')  t('phone.invalid', { n: 9, code: '+237' })
 */

import { createContext, useContext } from 'react';

/**
 * Contexte de langue : posé par les pages storefront (qui connaissent `store`),
 * consommé par les sous-composants via useStorefrontT() — évite de faire
 * transiter `store` dans chaque sous-composant.
 */
export const StorefrontLangContext = createContext('fr');

export function useStorefrontT() {
  return getStorefrontT(useContext(StorefrontLangContext));
}

/** Localiseur de textes marchands par défaut, branché sur le contexte de langue. */
export function useMerchantTextLocalizer() {
  const lang = useContext(StorefrontLangContext);
  return (value) => localizeMerchantDefault(lang, value);
}

